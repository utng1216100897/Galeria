     private void mostrarImagen() {

        try {
            Imagen imagen = lista.getActual().getDatos();
            if (lista.getActual() != null) {
                txtTitulo.setText(imagen.getTitulo());
                txtRuta.setText(imagen.getRuta());
                txtId.setText("" + imagen.getId());
               abrirImagen();

            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "No se puede mostrar la imagen");
        }

    }
    
