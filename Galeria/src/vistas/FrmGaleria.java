    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String pathFile = "";
        JFileChooser jfch = new JFileChooser();
        int op;
        op  = jfch.showSaveDialog(this);
        if (op == JFileChooser.APPROVE_OPTION){
            pathFile = jfch.getSelectedFile().getAbsolutePath();
            
            ImagenDOM imagenDOM = new ImagenDOM();
            imagenDOM.addAll(lista, pathFile);
            JOptionPane.showMessageDialog(this,"lista guardada");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnAbrirListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirListaActionPerformed
        limpiar();
        lista = new ListaImagenes();
        List<Imagen> imagenes;
        Imagen imagen;
        ImagenDOM imagenDOM = new ImagenDOM();

        String pathFile = "";
        JFileChooser jfch = new JFileChooser();
        int op;
        op = jfch.showOpenDialog(this);

        if (op == jfch.APPROVE_OPTION) {
            pathFile = jfch.getSelectedFile().getAbsolutePath();

            imagenes = imagenDOM.getAll(pathFile);

            for (int i = 0; i < imagenes.size(); i++) {
                imagen = imagenes.get(i);
                lista.insertarOrden(new NodoD(imagen));

            }

        }
        sacarLlista();
    }//GEN-LAST:event_btnAbrirListaActionPerformed

