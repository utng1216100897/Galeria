      public void ponerImagenLista(){
    
        lstNombres.setSelectedValue(txtTitulo.getText(), true);
        
    }
      
       public void sacarLlista() {

        lstNombres.setModel(new AbstractListModel<String>() {
            @Override
            public int getSize() {
                return lista.recorrerLista();
            }

            @Override
            public String getElementAt(int i) {

                return lista.sacarImagen(i);
            }

        });

    }
    
        private void limpiar() {
        txtTitulo.setText("");
        txtRuta.setText("");
        txtId.setText("");
        lblImagen.setIcon(null);
    }
       
